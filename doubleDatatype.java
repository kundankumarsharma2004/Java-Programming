package Basics;

public class doubleDatatype {
    public static void main(String[] args) {
        float x = 3.1415f;
        double y = 2.675;
        int z=5;
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(x+y+z);
    }
}
