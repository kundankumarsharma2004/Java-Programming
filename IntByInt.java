package Basics;

public class IntByInt {
    public static void main(String[] args) {
        double x=5;
        double y=2;
        System.out.println(x/y);
        double a=5/2;
        System.out.println(a);
        System.out.println(7/2);
        System.out.println(7.0/2.0);
        System.out.println(7.0/2);
        System.out.println(7/2.0);

    }
}
